	<h1>Participer</h1>
	Afin de centraliser les informations du développement du Robert, nous utilisons le <a href="https://github.com/" target="_new">système de partage de projet <b>GitHub</b></a>.
	<br />
	Voici un aperçu de ce que vous pouvez apporter au développement :
	<ul>
		<li>Rapporter un bug</li>
		<li>Proposer une idée</li>
		<li>Poser une question</li>
		<li>Ajouter des fonctions (cf. <a href='index.php?go=3wiki'>Wiki</a>)</li>
	</ul>
	<p>Par où commencer ? C'est facile :</p>
	<br />

	<h2>Création d'un compte sur GitHub</h2>

	<span class="floatLeft marge30r"><img src="gfx/git-tuto/create_account.jpg" width="500" border=2 /></span>
	<p>
		<br /><br />
		Pour pouvoir participer il faut vous connecter sur le site <a href="https://github.com/" target="_new">GitHub</a>.
	</p>
	<p>
		Si vous n'avez pas encore de compte GitHub, il faut en <b>créer un</b> (ça prend 10 secondes, et c'est <u>gratuit</u>) afin que nous puissions vous
		répondre ou vous demander des compléments d'information (pour reproduire un bug par exemple).
	</p>

