		<li>Reporter un bug</li>
		<li>Proposer une idée</li>
		<li>Poser une question</li>
		<li>Ajouter des fonctions ( <a href='index.php?go=3api'>page développeurs</a> )</li>
	</ul>

	<h2>Création d'un compte sur GitHub</h2>
	Pour pouvoir participer il faut vous connecter sur le site <a href="https://github.com/" target="_new">GitHub</a>.
	Si vous n'avez pas de compte dessus, il faut en créer un ( 10 secondes, c'est gratuit ) afin que nous puissions vous répondre / demander des compléments d'information ( pour reproduire un bug par exemple ).
	
