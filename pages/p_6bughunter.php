	<h2>Ajouter un bug</h2>
	Ci dessous la fenetre d'ajout de bug. ( <a href="https://github.com/RobertManager/robert/issues/new" target="_new"/>lien direct</a> )

	<p align ="center">
	<img src="gfx/git-tuto/add_bug.jpg" width="50%" border=2/></p>
	<u>Informations essentielles</u> :
	<ul>
		<li><b>Le titre</b> de votre ticket</li>
		<li><b>Sa description</b> Soyez le plus précis possible. Dans le cas d'un bug, essayez de le reproduire plusieurs fois et décrivez la procédure pour que nous puissions le corriger</li>
	</ul>
	<u>Les labels</u> permettent de catégoriser les informations que vous entrez :
	<ul>
		<li><b>Bug</b> - quand quelque chose ne se passe pas comme prévu</li>
		<li><b>Enhancement</b> - Si vous avez une idée de fonction ou d'amélioration</li>
		<li><b>Question</b> - Quelque chose que vous ne comprenez pas</li>
	</ul>

	<u>Mise en forme du texte</u> : Les raccourcis permettants de mettre votre texte en forme sont listés dans la fenetre qui s'ouvre en cliquant sur le lien 'GitHub Flavored Markdown'.
	Il s'agit d'un mode rapide de mise en forme du texte par exemple pour mettre en texte en gras il faut mettre deux étoiles ( multiplier sur le clavier ) qui entourent votre texte : <p align="center">**<b>Le texte a l'intérieur des étoiles est en gras</b>**</p>
	Quand vous avez finis, vous pouvez cliquer sur l'onglet preview pour voir le formattage.

	<p>Voila vous pouvez maintenant cliquer sur <b>SUBMIT NEW ISSUE</b>, pour que tous les développeurs reçoivent un mail qui les alerte de votre problème ou de votre suggestion</p>

